def set_language(lang: str) -> None:
    """Set the current language"""
    global _current_lang
    if lang in _translations:
        _current_lang = lang
        # Also update config
        try:
            from filepilot.core.config import load, save

            cfg = load()
            cfg["language"] = lang
            save(cfg)
        except Exception:
            pass


def get_language() -> str:
    """Get the current language"""
    return _current_lang


def t(key: str, **kwargs) -> str:
    """Translate a string

    Args:
        key: Translation key
        **kwargs: Format arguments

    Returns:
        Translated string, returns key itself if not found

    """
    text = _translations.get(_current_lang, {}).get(key)
    if text is None:
        text = _translations.get("en", {}).get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text


def load_language_from_settings() -> None:
    """Load language from user settings"""
    from filepilot.core.config import load as load_config

    try:
        cfg = load_config()
        lang = cfg.get("language", "en")
        set_language(lang)
    except Exception:
        pass
